from telegram import InlineQueryResultArticle, InputTextMessageContent, Update
from telegram.ext import Application, ContextTypes, InlineQueryHandler
import os
from datetime import datetime

request_no = 0

TOKEN = os.getenv('TOKEN')


async def inline_query(update: Update, context: ContextTypes.DEFAULT_TYPE) -> None:
	query = update.inline_query.query
	base_chars = "ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789"

	global request_no

	user_id = update.inline_query.from_user.id
	username = update.inline_query.from_user.username

	log(f'[#{request_no}] User ID: {user_id} | Username: @{username}')
	request_no += 1

	try:
		results = []
		index = 0
		for style_name, styled_chars in styles.items():
			translation_map = create_translation_map(base_chars, styled_chars)
			styled_text = query.translate(translation_map)
			results.append(InlineQueryResultArticle(id=index, title=styled_text, input_message_content=InputTextMessageContent(styled_text)))
			index += 1
		await update.inline_query.answer(results)
	except Exception as e:
		log(e)
		pass


def create_translation_map(base_chars, styled_chars):
	if len(base_chars) != len(styled_chars):
		raise ValueError("The lengths of base characters and styled characters must match.")
	return str.maketrans(base_chars, styled_chars)


